package com.arhat.chattest.adapter;

import android.content.Context;

import com.arhat.arhat.chattest.R;

import java.util.List;

/**
 * Created by lihanguang on 2016/9/28.
 */
public class GroupManagerAdapter extends CommontAdapter<String> {

    public GroupManagerAdapter(Context context, List<String> list, int layouId) {
        super(context, list, layouId);
    }

    @Override
    public void convert(ViewHolder holder, String item, int position) {
        holder.setText(R.id.tv_name, item);
    }
}
